    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String subject= SubjectIDComboBox.getSelectedItem().toString();
            SubjectDTO searchSubjectName = SubjectController.searchSubjectName(subject);
            
            
            StudentDTO student = new StudentDTO(
                    (String) StudentIDComboBox.getSelectedItem(),
                    txtRegistrationId.getText(),
                    txtFName.getText(),
                    txtLName.getText(),
                    txtDOB.getText(),
                    txtPhone.getText(),
                    txtAddress.getText(),
                    txtNID.getText(),
                    txtSchool.getText());
                    searchSubjectName.getSubjectID();
            boolean result = StudentController.updateStudent(student);

            String FName = txtRegistrationId.getText();

            if (FName.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Student name");
                txtRegistrationId.selectAll();
                txtRegistrationId.requestFocusInWindow();
                return;
            }
            String LName = txtRegistrationId.getText();
            if (LName.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Student name");
                txtLName.selectAll();
                txtLName.requestFocusInWindow();
                return;
            }

            String dob = txtDOB.getText();
            if (LName.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a DOB");
                txtDOB.selectAll();
                txtDOB.requestFocusInWindow();
                return;
            }

            String Phone = txtPhone.getText();
            if (Phone.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Phone number");
                txtPhone.selectAll();
                txtPhone.requestFocusInWindow();
                return;
            }
            String Address = txtAddress.getText();

            if (Address.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Address");
                txtAddress.selectAll();
                txtAddress.requestFocusInWindow();
                return;
            }
            String NID = txtNID.getText();

            if (NID.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a NIC");
                txtNID.selectAll();
                txtNID.requestFocusInWindow();
                return;
            }
            String School = txtSchool.getText();

            if (School.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a School");
                txtSchool.selectAll();
                txtSchool.requestFocusInWindow();
                return;
            }

            String Registration = txtRegistrationID.getText();

            if (Registration.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Registration");
                txtRegistrationID.selectAll();
                txtRegistrationID.requestFocusInWindow();
                return;
            }

            JOptionPane.showMessageDialog(this, "Student has been successfully Updated");
            txtStudentID.setText("");
            txtRegistrationId.setText("");
            txtLName.setText("");
            txtDOB.setText("");
            txtPhone.setText("");
            txtAddress.setText("");
            txtNID.setText("");
            txtSchool.setText("");
            txtRegistrationID.setText("");
            txtStudentID.setText(IdGenarator.getNewID("Student", "StudentID", "S"));
            txtRegistrationId.requestFocus();
        } catch (Exception ex) {
            Logger.getLogger(UpdateStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

