    private void loadStudentIds() throws Exception {
        ArrayList<StudentDTO> studentList = StudentController.getAllStudents();
        for (StudentDTO studentId : studentList) {
            StudentIDComboBox.addItem(studentId.getStudentID());

        }
    }

    private void loadRegistrationIds() throws Exception {
        ArrayList<StudentDTO> studentList = StudentController.getAllStudents();
        for (StudentDTO studentId : studentList) {
            StudentIDComboBox.addItem(studentId.getStudentID());

        }
    }

    private void loadSubjectIds() {
        try {
            ArrayList<SubjectDTO> subjectList = SubjectController.getAllSubjects();
            for (SubjectDTO subjectId : subjectList) {
                SubjectIDComboBox.addItem(subjectId.getSubjectName());

            }
        } catch (Exception ex) {
            Logger.getLogger(UpdateStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
