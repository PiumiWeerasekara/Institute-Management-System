    private void loadSubjects() throws Exception {
        try {
           ArrayList<SubjectDTO>subjectList=SubjectController.getAllSubjects();
            for (SubjectDTO subjectId : subjectList) {
                SubjectComboBox.addItem(subjectId.getSubjectID());
                
            }
        } catch (IOException ex) {
            Logger.getLogger(AddTeacherForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
