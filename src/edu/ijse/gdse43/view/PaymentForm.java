    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
       
        
        try {
            ArrayList<PaymentDTO> alPayments = PaymentController.getAllPayments();
            DefaultTableModel dtm=(DefaultTableModel)tblPayment.getModel();
            dtm.setRowCount(0);
            for (PaymentDTO payment : alPayments) {
                Object[] rowData = {payment.getPaymentID(),
                    payment.getStudentID(),
                    payment.getSubjectID(),
                    payment.getMonth(),
                    payment.getAmount(),
                };
                dtm.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        
        
    }//GEN-LAST:event_btnViewActionPerformed

