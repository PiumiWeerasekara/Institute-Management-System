    private void btnAddPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPaymentActionPerformed
        try {
            PaymentDTO payment = new PaymentDTO(
                    txtPaymentId.getText(),
                    txtStudentID.getText(),
                    SubjectComboBox.getSelectedItem().toString(),
                    txtMonth.getText(),
                    Integer.parseInt(txtAmount.getText()));
            boolean result = PaymentController.addPayment(payment);
            if (result) {
                JOptionPane.showMessageDialog(this, "Payment has been sucessfully added");
            } else {
                JOptionPane.showMessageDialog(this, "Payment hasn't been sucessfully added");
            }
        } catch (Exception ex) {
            Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtStudentID.setText("");
            txtMonth.setText("");
            txtAmount.setText("");
        

    }//GEN-LAST:event_btnAddPaymentActionPerformed

