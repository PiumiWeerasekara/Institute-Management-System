    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed

        try {

            ArrayList<SheduleDTO> alShedules = SheduleController.getAllShedules();
            DefaultTableModel dtm = (DefaultTableModel) tblShedule.getModel();
            dtm.setRowCount(0);
            for (SheduleDTO shedule : alShedules) {
                Object[] rowData = {shedule.getSheduleID(),
                    shedule.getBatchID(),
                    shedule.getTeacherID(),
                    shedule.getSubjectID(),
                    shedule.getDate(),
                    shedule.getStartTime(),
                    shedule.getEndTime()
                };

                dtm.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(ScheduleForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnViewActionPerformed

