    private void loadSystemDate() {
        Date d1 = new Date();
        SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
        String FormatDate = df.format(d1);
        txtDate.setText(FormatDate);

    }

    private void loadSystemTime() {
        new Timer(0, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                Date d1 = new Date();
                SimpleDateFormat df = new SimpleDateFormat("hh:mm:ss a");
                String FormatDate = df.format(d1);
                txtTime.setText(FormatDate);
            }
        }).start();
    }

    private void loadBatchIDs() throws Exception {
        ArrayList<BatchDTO> batchList = BatchController.getAllBatches();
        for (BatchDTO batchId : batchList) {
            BatchIDComboBox.addItem(batchId.getBatchNo());

        }
    }

    private void loadTeacherIDs() throws Exception {
        ArrayList<TeacherDTO> teacherList = TeacherController.getAllTeachers();
        for (TeacherDTO teacherId : teacherList) {
            TeacherIDComboBox.addItem(teacherId.getName());

        }
    }

    private void loadSubjectIDs() {
        try {
            ArrayList<SubjectDTO> subjectList = SubjectController.getAllSubjects();
            for (SubjectDTO subjectID : subjectList) {
                SubjectIDComboBox.addItem(subjectID.getSubjectName());

            }
        } catch (Exception ex) {
        }
    }

